# declaring variables and setting types
a = 3               # integer
a2 = -3             # integer
b = 2.2             # float
b2 = 3/2            # float
c = "Hello"         # str
d = True            # bool

# print types
print(type(a))
print(type(a2))
print(type(b))
print(type(b2))
print(type(c))
print(type(d))

